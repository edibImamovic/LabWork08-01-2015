package ba.bitcamp.linkedlist1;

public class Person {

	private String name;
	
	public Person (String name){
		
		this.name = name;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
}
